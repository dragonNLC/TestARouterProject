package com.dragon.test.arouter2;

public class TestData {

    private String data;
    private int num;

    public TestData() {
    }

    public TestData(String data, int num) {
        this.data = data;
        this.num = num;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }


}
